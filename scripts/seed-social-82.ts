async function main() {
  const existing = await prisma.question.findFirst({
    where: { courseId: "course-social", question: QUESTIONS[0].question },
  })

  if (existing) {
    console.log("Social questions 82-91 already seeded, skipping")
    return
  }

  let added = 0
  for (const q of QUESTIONS) {
    await prisma.question.create({
      data: { courseId: "course-social", ...q, position: 200, sourceType: "Uploaded" },
    })
    added++
  }

  console.log(`Added ${added} new questions to course-social (82-91)`)
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect())
