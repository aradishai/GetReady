async function main() {
  // Get all course-social questions with position=0, ordered by createdAt ASC (insertion order)
  const dbQuestions = await prisma.question.findMany({
    where: { courseId: "course-social", position: 0 },
    orderBy: { createdAt: "asc" },
    select: { id: true, question: true, createdAt: true },
  })

  if (dbQuestions.length === 0) {
    console.log("No course-social questions found, skipping restore")
    return
  }

  // Check idempotency: if the last DB question (insertion order) already matches decoded Q1
  const lastDbQ = dbQuestions[dbQuestions.length - 1]
  const expectedQ1 = QUESTIONS[0].question
  if (lastDbQ.question === expectedQ1) {
    console.log("Social questions already up to date, skipping restore")
    return
  }

  console.log(`Updating ${dbQuestions.length} social questions...`)

  // Mapping: DB at createdAt-ASC index j → QUESTIONS[73-j]
  let updated = 0
  for (let j = 0; j < dbQuestions.length; j++) {
    const targetIdx = (dbQuestions.length - 1) - j
    if (targetIdx >= QUESTIONS.length) continue

    const newData = QUESTIONS[targetIdx]
    await prisma.question.update({
      where: { id: dbQuestions[j].id },
      data: {
        question: newData.question,
        answerA: newData.answerA,
        answerB: newData.answerB,
        answerC: newData.answerC,
        answerD: newData.answerD,
        correctAnswer: newData.correctAnswer,
        explanation: newData.explanation,
        difficulty: newData.difficulty,
        topic: newData.topic,
      },
    })
    updated++
  }

  console.log(`Done. Updated ${updated} questions.`)
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect())